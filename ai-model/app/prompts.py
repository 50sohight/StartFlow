"""Файл с промптами"""

def description_prompt() -> str:
    DESCRIPTION_USER_QUERY = """
Сформируй вводное описание проекта для нового участника команды.

Входные данные:

* Описание проекта: {{project_description}}
* Задачи за весь период: {{tasks}}

На основе описания проекта и задач объясни, о чем проект, что в нем реализуется, какой стек используется, что команда уже сделала, что сейчас находится в работе и что планируется.

Правила:

* используй только переданные данные;
* не выдумывай технологии, функции, участников, сроки и результаты;
* если стек указан в описании или задачах, обязательно упомяни его;
* завершенные задачи описывай как сделанные;
* незавершенные задачи описывай как текущие или планируемые работы;
* если данных мало, все равно дай краткое описание по имеющейся информации;
* пиши «данных недостаточно» только если нет ни описания проекта, ни содержательных задач.
* не пиши лишние цифры, комментарии по типу "Вот ваш ответ".

Структура ответа:

1. Общее описание проекта.
2. Что сейчас реализуется.
3. Используемый стек.
4. Что уже сделано.
5. Что находится в работе или планируется.
6. Краткий вывод для новичка.

Пиши связным, понятным текстом без перечисления всех задач подряд. Ответ никак не форматируй, можешь использовать только переносы строк.
"""

    return DESCRIPTION_USER_QUERY.strip()

def report_prompt() -> str:
    REPORT_USER_QUERY = """
Проанализируй данные проекта и сформируй отчет за период.

Входные данные:
* Описание проекта: {{project_description}}
* Задачи за период: {{tasks}}

Правила:
1. Используй только переданные данные: описание проекта, задачи, статусы, дедлайны и исполнителей.
2. Не выдумывай технологии, роли, результаты, причины задержек и выполненные работы.
3. Описание проекта используй как контекст, чтобы понять, над чем работает команда.
4. Если задач за период нет, напиши, что за данный период задачи не создавались или активность не зафиксирована.
5. Не пиши «данных недостаточно», если есть хотя бы описание проекта или содержательные задачи. Используй эту фразу только при полном отсутствии полезных данных.
6. Выполненными считай только задачи со статусом завершения. Остальные описывай как находящиеся в работе, запланированные, незавершенные или просроченные, если это видно по статусу и дедлайну.
7. Участников указывай в формате «Фамилия И.О.». Если преобразовать имя нельзя, используй его как в данных.
8. Не перечисляй все задачи подряд: обобщай их по смыслу и направлениям.
9. Текст возвращай в формате Markdown

Структура ответа:
1. Общая характеристика работ
   Кратко опиши, что создавалось или дорабатывалось в проекте за период.
2. Основные направления
   Выдели 2–5 направлений работы, например backend, frontend, база данных, авторизация, логирование, интеграции, инфраструктура, тестирование. Указывай только то, что подтверждается задачами или описанием проекта.
3. Отчетность по участникам
   Для каждого исполнителя напиши отдельный связный абзац: чем он занимался, что завершил, что осталось в работе, не было завершено или просрочено. Не используй маркированные списки внутри этого раздела.
4. Итог
   Кратко сформулируй общий результат за период и текущее состояние работ.

Стиль: деловой, краткий, без воды и неподтвержденных предположений. Если вывод косвенный, используй формулировки: «по задачам видно», «судя по статусам», «в рамках периода команда занималась».
Ответ никак не форматируй, можешь использовать только переносы строк.
"""
    return REPORT_USER_QUERY.strip()