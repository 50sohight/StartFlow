"""Файл с промптами"""

def description_prompt() -> str:
    DESCRIPTION_USER_QUERY = """
Сформируй готовое описание проекта на русском языке, начиная с большой буквы на основе переданных данных.

Используй только факты из переданных документов.
Не придумывай технологии, библиотеки, функции, статусы задач, сроки, цели или прогресс, если их нет в данных.

Если в документах есть хотя бы часть информации о проекте, задачах, технологиях или прогрессе, НЕ отказывайся от ответа.
Не проси предоставить дополнительные данные.
Не используй фразы: "к сожалению", "я не могу", "у меня нет данных", "если бы у меня были данные", "пожалуйста, предоставьте".
Сразу начинай с готового описания проекта.

Если часть информации отсутствует, просто не упоминай этот блок.
Если данных мало, сформируй короткое описание только по доступной информации.

Текст должен быть естественным, связным и пригодным для отображения на фронтенде в разделе описания проекта.
Описание должно быть логически связным, комфортным для чтения.
Начинай с сути проекта: что это за проект и для чего он нужен.

Обязательно сохрани конкретику, если она есть в данных:
Опиши подробно какая именно логика реализуется, что используется в проекте, сформируй по описанию задач и их выполнению ход работы проекта.

Структура ответа:
1. Первый абзац — общее описание проекта, его назначение и цель.
2. Второй абзац — реализованные функции и текущие работы.
3. Третий абзац — используемые технологии, библиотеки и текущий прогресс.
4. Четвертый абзац — зоны внимания или оставшиеся доработки, если они есть.

Стиль:
- деловой, но живой и читаемый;
- без markdown;
- без нумерованных списков;
- без рассуждений о том, что данных недостаточно;
- только готовое описание проекта.

Если документы действительно пустые или не содержат информации о проекте, верни ровно этот текст:
Недостаточно данных для формирования описания проекта.
"""

    return DESCRIPTION_USER_QUERY.strip()