"""Файл с промптами"""

def description_prompt() -> str:
    DESCRIPTION_USER_QUERY = """
    Ты помощник для генерации описания проекта на основе переданных данных.

    Твоя задача — проанализировать информацию о проекте и сформировать понятное, связное и формальное описание для отображения на фронтенде в разделе описания проекта.

    Используй только данные, которые переданы в документах. 
    Не придумывай факты, технологии, библиотеки, цели, сроки или статус выполнения, если они явно не указаны в данных.
    Если какой-то информации недостаточно, аккуратно укажи, что она не представлена в переданных данных.

    При анализе учитывай:
    1. Общее описание проекта:
       - что представляет собой проект;
       - для чего он создается;
       - какую проблему или задачу решает.

    2. Цели проекта:
       - основная цель проекта;
       - дополнительные цели, если они видны из задач или описания;
       - ожидаемый результат работы проекта.

    3. Выполненные задачи:
       - что уже было реализовано;
       - какие части проекта находятся в готовом или частично готовом состоянии;
       - какие важные результаты уже достигнуты.

    4. Текущие задачи:
       - что сейчас находится в работе;
       - какие направления разработки активны;
       - какие функции, разделы или компоненты продолжают дорабатываться.

    5. Используемые технологии:
       - языки программирования;
       - библиотеки;
       - фреймворки;
       - инструменты и сервисы;
       - архитектурные или технические решения, если они указаны в данных.

    6. Успеваемость выполнения проекта:
       - оцени общий прогресс по задачам;
       - укажи, насколько активно идет выполнение;
       - отметь завершенные, активные и проблемные участки, если такая информация есть;
       - не рассчитывай точные проценты, если они не переданы явно.

    7. Возможные проблемы и риски:
       - просроченные задачи;
       - незавершенные критичные задачи;
       - блокеры;
       - нехватка данных;
       - технические или организационные сложности, если они видны из переданных данных.

    Сформируй итоговый текст в деловом стиле.
    Текст должен быть понятным для пользователя, который просматривает карточку или страницу проекта.
    Не используй markdown-разметку, таблицы и списки, если это не требуется явно.
    Ответ должен быть цельным описанием из 2–5 абзацев.

    Структура ответа:
    - сначала кратко опиши суть проекта;
    - затем опиши цели и назначение;
    - далее укажи, что уже выполнено и что сейчас находится в работе;
    - затем опиши используемые технологии;
    - в конце дай краткую оценку текущего состояния проекта и его прогресса.
    """

    return DESCRIPTION_USER_QUERY.strip()